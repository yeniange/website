$(document).ready(function() {
	$("#odeurang").show();
	$("#odeurangInfo").hide();
	$("span.more01").hide();
	
	$("span.more1").click(function() {
		$("#odeurangInfo").show('3000');
		$(this).hide('fast');
		$("span.more01").show('fast');	
	});
	
	$("span.more01").click(function() {
		$("#odeurangInfo").hide('3000');
		$(this).hide('fast');
		$("span.more1").show('fast');	
	});
});
